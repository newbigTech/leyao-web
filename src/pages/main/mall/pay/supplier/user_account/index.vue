          <el-breadcrumb separator=">">
            <el-breadcrumb-item>资产管理</el-breadcrumb-item>
            <el-breadcrumb-item>用户余额</el-breadcrumb-item>
          </el-breadcrumb>
      </div>
      <component :is="currentComponent" :model="model" @forward="forward" @reback="reback"></component>
    </div>
</template>
<script>

import userAccountList from "./components/user-account-list"
import userAccountTransactionList from "./components/user-account-transaction-list"


export default {
    name: 'user-account-list',
    components: { userAccountList,userAccountTransactionList },
    data() {
      return {
        currentComponent: 'userAccountList',
        model: {}
      }
    },

    methods: {
        forward(comp, data) {
          this.currentComponent = comp
          this.model = data || {}
        },
        reback(comp, data) {
          this.currentComponent = comp
          this.model = data || {}
        },
    },
    created() {
    }
}
</script>
